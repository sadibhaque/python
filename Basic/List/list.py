#index  0   1   2   3   4   5   6
num = [10, 11, 34, 65, 63, 87, 90]
#index -7  -6  -5  -4  -3  -2  -1

print(num[-1]) # printing first index from last

print(num[2:6]) # printing index 2 to 5

print(num[2:7:2]) # printing index 2 to 6 but skipping 1 index

print(num[6:2:-1]) # printing index 6 to 2 but in reverse order

print(num[:]) # printing all

print(num[4:]) # printing from 4 to end 

print(num[:5]) # printing from start to 4
